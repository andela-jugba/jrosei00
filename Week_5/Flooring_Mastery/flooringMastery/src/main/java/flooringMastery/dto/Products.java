package flooringMastery.dto;

import java.math.BigDecimal;

/**
 *
 * @author apprentice
 */
public class Products {

    private String product;
    private BigDecimal areaCost;
    private BigDecimal laborCost;

    /**
     * @return the product
     */
    public String getProduct() {
        return product;
    }

    /**
     * @param product the product to set
     */
    public void setProduct(String product) {
        this.product = product;
    }

    /**
     * @return the areaCost
     */
    public BigDecimal getAreaCost() {
            return areaCost;
    }

    /**
     * @param areaCost the areaCost to set
     */
    public void setAreaCost(BigDecimal areaCost) {
        this.areaCost = areaCost;
    }

    /**
     * @return the laborCost
     */
    public BigDecimal getLaborCost() {
        return laborCost;
    }

    /**
     * @param laborCost the laborCost to set
     */
    public void setLaborCost(BigDecimal laborCost) {
        this.laborCost = laborCost;
    }
}
